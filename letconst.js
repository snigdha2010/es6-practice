//const example
const age = 12; 
console.log(age);
//age = 10; age can not be changes 
console.log(age);

//const array variable element can be change , full array cannot be changed
const names = ["jaber","saber"];
console.log(names);
names[0]= "mina";
console.log(names);
names.push(12);
console.log(names);
//names = ["rina","tina"]; const full array can not be changes 
console.log(names);


//let full array can be changes 
let items = [12,13,14];
console.log(items);
items = [12];
console.log(items);

